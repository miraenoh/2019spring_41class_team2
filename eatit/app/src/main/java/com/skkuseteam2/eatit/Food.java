package com.skkuseteam2.eatit;

public class Food {

    private int id;
    private String name;
    private String photo;
    private int price;
    private int prop_hot;
    private int prop_sweet;
    private int prop_sour;
    private int prop_cal;
    private int prop_soup;
    private int prop_main;
    private int prop_temp;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getProp_hot() {
        return prop_hot;
    }

    public void setProp_hot(int prop_hot) {
        this.prop_hot = prop_hot;
    }

    public int getProp_sweet() {
        return prop_sweet;
    }

    public void setProp_sweet(int prop_sweet) {
        this.prop_sweet = prop_sweet;
    }

    public int getProp_sour() {
        return prop_sour;
    }

    public void setProp_sour(int prop_sour) {
        this.prop_sour = prop_sour;
    }

    public int getProp_cal() {
        return prop_cal;
    }

    public void setProp_cal(int prop_cal) {
        this.prop_cal = prop_cal;
    }

    public int getProp_soup() {
        return prop_soup;
    }

    public void setProp_soup(int prop_soup) {
        this.prop_soup = prop_soup;
    }

    public int getProp_main() {
        return prop_main;
    }

    public void setProp_main(int prop_main) {
        this.prop_main = prop_main;
    }

    public int getProp_temp() {
        return prop_temp;
    }

    public void setProp_temp(int prop_temp) {
        this.prop_temp = prop_temp;
    }
}
